package kg.salongo.android.Data;

public class Satus {
    int typeStatus;
    String status_Free;
    String status_busy;

    public int getTypeStatus() {
        return typeStatus;
    }

    public void setTypeStatus(int typeStatus) {
        this.typeStatus = typeStatus;
    }

    public String getStatus_busy() {
        return status_busy;
    }

    public void setStatus_busy(String status_busy) {
        this.status_busy = status_busy;
    }

    public String getStatus_Free() {
        return status_Free;
    }

    public void setStatus_Free(String status_Free) {
        this.status_Free = status_Free;
    }
}
